package data.types;

public class DataTypes {

   
    public static void main(String[] args) {
        
        int n= 5; 
        float f = 5.99f; 
        double d= 53.99; 
        char c = '['; 
        boolean b= true; 
        String s= "Hello"; 

        System.out.println("Integer-Variable " + n);
        System.out.println("Float-Variable " + f);
        System.out.println("Double-Variable " + d);
        System.out.println("Boolean-Variable " + b);
        System.out.println("char-Variable " + c);
        System.out.println("string-Variable " + s);
    }
    }
    